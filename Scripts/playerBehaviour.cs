    //Array of type of solid - type Object specific of Unity
    public Transform[] typeOfSolid = new Transform[7];
    //Array of Y-AXIS of the solids
    public float[] yAxis = new float[] {0.5f, 1.5f, 0.5f, 0.5f, 1.14f, 0.417f, 0.5f};
